package org.ei.tags;

public class EditTag
{

      private String editFieldName; 
	  private String editHFieldName;
	  private String editLable; 
	  private String editTagsName; 
	  private String editScope;
	  private String editGroupID;
	 
	  
	  
	public String getEditFieldName() {
		return editFieldName;
	}
	public void setEditFieldName(String editFieldName) {
		this.editFieldName = editFieldName;
	}
	public String getEditHFieldName() {
		return editHFieldName;
	}
	public void setEditHFieldName(String editHFieldName) {
		this.editHFieldName = editHFieldName;
	}
	
	public String getEditTagsName() {
		return editTagsName;
	}
	public void setEditTagsName(String editTagsName) {
		this.editTagsName = editTagsName;
	}
	public String getEditScope() {
		return editScope;
	}
	public void setEditScope(String editScope) {
		this.editScope = editScope;
	}
	public String getEditLable() {
		return editLable;
	}
	public void setEditLable(String editLable) {
		this.editLable = editLable;
	}
	public String getEditGroupID() {
		return editGroupID;
	}
	public void setEditGroupID(String editGroupID) {
		this.editGroupID = editGroupID;
	}
}
