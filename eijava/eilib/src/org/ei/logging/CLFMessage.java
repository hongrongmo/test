package org.ei.logging;


public class CLFMessage
{
    private String clfMessage;

    public CLFMessage(String message)
    {
        this.clfMessage = message;
    }

    public String getCLFMessage()
    {
        return this.clfMessage;
    }

    public void setCLFMessage(String clfMessage)
    {
        this.clfMessage = clfMessage;
    }

}
