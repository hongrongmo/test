package org.ei.stripes.view;

public class Doc {
	private int hitindex;
	private String docid;
	private String dbid;
	private int dbmask;
	private String dbname;
	private String dbsname;
	private String dbiname;

	public int getHitindex() {
		return hitindex;
	}

	public void setHitindex(int hitindex) {
		this.hitindex = hitindex;
	}

	public String getDocid() {
		return docid;
	}

	public void setDocid(String docid) {
		this.docid = docid;
	}

	public String getDbid() {
		return dbid;
	}

	public void setDbid(String dbid) {
		this.dbid = dbid;
	}

	public int getDbmask() {
		return dbmask;
	}

	public void setDbmask(int dbmask) {
		this.dbmask = dbmask;
	}

	public String getDbname() {
		return dbname;
	}

	public void setDbname(String dbname) {
		this.dbname = dbname;
	}

	public String getDbsname() {
		return dbsname;
	}

	public void setDbsname(String dbsname) {
		this.dbsname = dbsname;
	}

	public String getDbiname() {
		return dbiname;
	}

	public void setDbiname(String dbiname) {
		this.dbiname = dbiname;
	}
}
