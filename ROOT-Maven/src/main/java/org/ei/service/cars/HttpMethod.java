package org.ei.service.cars;

/**
 * Enum class to hold all http methods which will be used
 * for making RESTFul service call
 * 
 * @author naikn1
 * @version 1.0
 *
 */
public enum HttpMethod {
    /**
     * Represents http GET method
     */
	GET,
	/**
	 * Represents http POST method
	 */
    POST,
    /**
     * Represents http PUT method
     */
    PUT,
    /**
     * Represents http DELETE method
     */
    DELETE;
}

